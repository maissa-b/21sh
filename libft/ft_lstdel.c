#include "libft.h"

void		ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;

	if (alst && del)
	{
		tmp = *alst;
		while (*alst)
		{
			*alst = (*alst)->next;
			ft_lstdelone(&tmp, del);
			tmp = *alst;
		}
	}
}
